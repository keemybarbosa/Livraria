    public static int menu(String cabecalho, Scanner scanner, String[] menu, String menuSaida) {
        int numero = -1;

        do {
            clearConsole();
            System.out.println(cabecalho);
            System.out.println("Digite uma das opcoes: ");

            System.out.println("[0] - " + menuSaida);
            for (int i = 0; i < menu.length; i++) {
                System.out.printf("[%d] - %s%n",i+1,menu[i]);
            }

            if (scanner.hasNextInt()){
                numero = scanner.nextInt();
                if(numero < 0 || numero > menu.length){
                    System.out.println("##### Opção Inválida! #####");
                    clearConsole();
                }
            } else {
                System.out.println("Entrada inválida!");
                //Limpando o buffer
                scanner = new Scanner(System.in);
            }

        }while(numero < 0 || numero > menu.length);
        return numero;
    }

